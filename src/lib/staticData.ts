export function getSeasonTasks(dateStr: string) {
  // dateStr is like "08.04.2026"
  const parts = dateStr.split('.');
  if (parts.length >= 2) {
    const month = parseInt(parts[1], 10);
    if (month >= 3 && month <= 5) return STATIC_TASKS.spring;
    if (month >= 6 && month <= 8) return STATIC_TASKS.summer;
    if (month >= 9 && month <= 11) return STATIC_TASKS.autumn;
    return STATIC_TASKS.winter;
  }
  return STATIC_TASKS.spring; // fallback
}
